
!function(){
 
  let btn = document.querySelector('.burger__btn1');
  let el = document.querySelector('.burger__menu');
  
  btn.addEventListener('click', () => {
    el.style.display === 'none' ? el.style.display = 'block' : el.style.display = 'none';
  });
  
  
  

}()







